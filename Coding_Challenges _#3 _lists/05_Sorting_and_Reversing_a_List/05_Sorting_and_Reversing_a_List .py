# Writing List of fruits
fruits = ['banana', 'apple', 'grape', 'kiwi', 'orange']
# Sorting the list in alphabetical order (ascending
fruits.sort()
# Printing a fruits list
print(fruits)
# Printing the reversed sorted list
fruits.sort(reverse=True)
# Printing the updated fruit list
print(fruits)
